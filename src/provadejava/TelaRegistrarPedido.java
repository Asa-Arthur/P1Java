    private void btnRegistrarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarPedidoActionPerformed
        // TODO add your handling code here:
        try {
            Pedido pedido = new Pedido();
            PedidoDAO pedidoDAO = new PedidoDAO();

            // Define data atual
            pedido.setData(new java.sql.Timestamp(System.currentTimeMillis()));

            String nomeSelecionado = (String) cmbClienteFornecedor.getSelectedItem();
            if (rbtnCliente.isSelected()) {
                pedido.setTipoNota(1);
                Cliente cliente = mapaClientes.get(nomeSelecionado);
                pedido.setIdCliente(cliente.getId());

         } else if (rbtnFornecedor.isSelected()) {
             pedido.setTipoNota(2);
             Fornecedor fornecedor = mapaFornecedores.get(nomeSelecionado);
             pedido.setIdFornecedor(fornecedor.getId());
          }

          // Chama o DAO
          pedidoDAO.registrarPedido(pedido);

          JOptionPane.showMessageDialog(this, "Pedido registrado com sucesso!");

      } catch (Exception e) {
          JOptionPane.showMessageDialog(this, "Erro ao registrar pedido: " + e.getMessage());
          e.printStackTrace();
        }
    }//GEN-LAST:event_btnRegistrarPedidoActionPerformed

