    private Map<String, Cliente> mapaClientes = new HashMap<>();
    private Map<String, Fornecedor> mapaFornecedores = new HashMap<>();
    
    public void carregarClientes() {
        cmbClienteFornecedor.removeAllItems();
        mapaClientes.clear();

        ClienteDAO cDAO = new ClienteDAO();
        List<Cliente> lista = cDAO.getCliente();
        
        if (lista != null) {
            for (Cliente c : lista) {
                String chave = c.getNome(); // ou nomeFantasia, se for o que aparece na tela
                mapaClientes.put(chave, c);
                cmbClienteFornecedor.addItem(chave);
            }
        }
    }

    
    private void carregarFornecedores() {
        cmbClienteFornecedor.removeAllItems();
        mapaFornecedores.clear();
        
        FornecedorDAO fDAO = new FornecedorDAO();
        List<Fornecedor> lista = fDAO.getFornecedor();
        
        if (lista != null) {
            for (Fornecedor f : lista) {
                String chave = f.getNome(); // idem
                mapaFornecedores.put(chave, f);
                cmbClienteFornecedor.addItem(chave);
            }
        }
    }


